console.log("This is the Example of the switch case Example")

var number = 1000
switch (number){
    case 100:
        console.log("The Value is 100 okay:")
        break;
    case 200:
        console.log("The Value is 200 Okay:")
        break;
    case 300:
        console.log("The value is 300 okay:")
        break;
    default:
        console.log("No Number is Found okay :")

}